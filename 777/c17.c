#include <stdio.h>

int main(){
  int a,b;
  printf("请输入符号总数:");
  scanf("%d",&a);
  /*输入变量 总数 的值*/

  printf("请输入每行数量:");
  scanf("%d",&b);
  /*输入变量 行数 的值*/
  
  while(a>=b){

    if(a%b==0){
      printf("|\n");
    }

    printf("|\t");
    /*--------总数 小于 行数 则打印*号-------*/

    /*--------如果 总数 除行数的余数为 0 则打印换行------*/
    a=a-1;
    /*--------变量a 加1-------*/
  }
  
  return 0;
}
